package pl.zajavka;

public class Calculator {

    public static int add(int left, int right){
        return left + right;
    }
    public static int subtract(int left, int right){
        return left - right;
    }

    public static int multiple(int left, int right){
        return left * right;
    }

    public static int divide(int left, int right){
        return left / right;
    }
}
